console.log(2 ^ 4);

//output :- 6
/*
    The output of the above command is 6.
    Here, we are performing XOR operation between integers 2 and 4
    In XOR operation, if any of the bit is 1, it outputs 1
    If both the bits are 0 or 1 , then it outputs 0
    So, here in this case,
    binary representation of 2 and 4 is
    0 1 0 ---> 2
    1 0 0 ---> 4
    --------
    1 1 0 ---> 6
    which is the answer
*/